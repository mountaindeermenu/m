        function toggleCategory(element) {
            var products = element.querySelector('.products');
            if (products.classList.contains('open')) {
                products.classList.remove('open');
                element.classList.remove('open');
                element.classList.add('closed');
            } else {
                document.querySelectorAll('.products.open').forEach(function(product) {
                    product.classList.remove('open');
                    product.parentElement.classList.remove('open');
                    product.parentElement.classList.add('closed');
                });
                products.classList.add('open');
                element.classList.remove('closed');
                element.classList.add('open');
            }

        }
    